const CHINA_CITY_NAME_SET: ReadonlySet<string> = new Set(CHINA_CITY_NAMES);

export function normalizeChinaCityName(name: string): string {
  const trimmed = (name ?? '').trim();
  if (!trimmed) return '';

  if (trimmed === '喀什地区') return '喀什';
  if (trimmed === '伊犁哈萨克自治州') return '伊犁';
  if (trimmed === '西双版纳傣族自治州') return '西双版纳';
  if (trimmed === '延边朝鲜族自治州') return '延边';
  if (trimmed === '黔东南苗族侗族自治州') return '黔东南';

  if (trimmed.endsWith('市')) {
    const short = trimmed.slice(0, -1);
    if (CHINA_CITY_NAME_SET.has(short)) return short;
  }

  return trimmed;
}

export function isChinaCityName(name: string): boolean {
  const normalized = normalizeChinaCityName(name);
  return CHINA_CITY_NAME_SET.has(normalized);
}
